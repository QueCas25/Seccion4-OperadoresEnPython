# Operadores de comparacion

a = 4
b = 2

resultado = a == b
# lo que esta al lado derecho se evalua primera y luego se asigna al lado izquierdo

print(f'Resultado == : {resultado}')

# DIFERENTE DE
resultado = a != b
print(f'Resultado != : {resultado}')

#MAYOR QUE
resultado = a > b
print(f'Resultado >: {resultado}')

# MENOR QUE
resultado = a < b
print(f'Resultado <: {resultado}')

# MENOR  IGUAL QUE
resultado = a <= b
print(f'Resultado <=: {resultado}')

# MAYOR IGUAL QUE
resultado = a >= b
print(f'Resultado >=: {resultado}')

