# Operadores de asignacion
miVariable = 10
print(miVariable)

miVariable = miVariable + 1

# incremento con reasignacion

miVariable+= 1
print(miVariable)

# decremento con reasignacion
miVariable -= 2
print(miVariable)

# multiplicacion con reasignacion
miVariable *= 3
print(miVariable)

#division con reasignacion
miVariable /= 2
print(miVariable)
