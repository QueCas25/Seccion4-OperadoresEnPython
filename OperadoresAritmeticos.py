# Opeeradores Aritmeticos

operandoA= 3
operandoB= 2

suma = operandoA + operandoB
print('resultado de la suma: ', suma)
print(f'Resultado suma: {suma}')
# se llama interpolacion, incluir variables en una cadena

#Operador aritmetico de resta

resta = operandoA - operandoB
print(f'Resultado de la resta: {resta}')

#Operador aritmetico de multiplicacion
multiplicacion = operandoA * operandoB
print(f'Resultado de la multiplicacion: {multiplicacion}')

#Operador aritmetico de la division
division = operandoA / operandoB
print(f'Resultado de la division: {division}')

# Operador aritmetico de la division pero en ENTERO
division = operandoA // operandoB
print(f'Resultado de la divisiom ENTERA: {division}')

#Operador aritmetico de modulo
modulo = operandoA % operandoB
print(f'Resultado de la operacion de modulo: {modulo}')

#Operador de modulo exponente
exponente = operandoA ** operandoB
print(f'Resultado del exponente: {exponente}')