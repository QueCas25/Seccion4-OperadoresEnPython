# Operadores Logicos

a= True
b = False

# AND
resultado = a and b
print(resultado)

# OR
resultado = a or b
print(resultado)

# NOT
# Aqui se invierte el resultado de la variable, es TRUE pero al aplicar NOT se convierte a FALSE
resultado = not a
resultado = not b
print(resultado)
# Este operador es unario, solo necesita un operando para trabajar,
# AND Y ORD son binarios ya que necesitan 2 operandos para trabajar
